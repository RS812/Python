a=int(input("Enter Value of A:"))
b=int(input("Enter value of B:"))
def Addition(x,y):
    return(x+y)
def Subtraction(x,y):
    return(x-y)
def Multiplication(x,y):
    return(x*y)
def Division(x,y):
    return(x/y)
def Modulus(x,y):
    return(x%y)
